type TextBundle = typeof TEXT_ZH | typeof TEXT_ZH_TW | typeof TEXT_EN;

const TEXT_BY_LANG: Record<AppLang, TextBundle> = {
  zh: TEXT_ZH,
  "zh-TW": TEXT_ZH_TW,
  en: TEXT_EN,
};

export function useI18n() {
  const lang = useStore((s) => s.lang);
  const t = TEXT_BY_LANG[lang] ?? TEXT_ZH;
  return { t, lang };
}
